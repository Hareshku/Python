class Account:
  def __init__(self, account=234544454545, balance=0):
    self.account=account
    self.balance= balance
    # print(account, balance)
  
  def debit(self, amount):
    self.balance-=amount
    print("Rs : ", amount," was debited \nRemaining balance is Rs : ", self.getBalance())

  
  def credit(self, amount):
    self.balance+=amount
    print("Rs : ", amount," was credited\nTotal balance is Rs : ",self.getBalance())
    
  
  def getBalance(self):
    return self.balance

acc1=Account(23232323, 1000)
print("Initial balance is Rs : ", acc1.getBalance())
acc1.credit(2000)
acc1.debit(500)
